package com.imwsoftware.mongo.model;

public class Subject {

	private String name;
	private String prof;

	public Subject() {

	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getProf() {
		return this.prof;
	}

	public void setProf(String prof) {
		this.prof = prof;
	}
}
